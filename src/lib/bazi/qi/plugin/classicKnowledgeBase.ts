/** 返回全部条目 */
export function getKnowledgeBase(): ClassicKnowledgeEntry[] {
  return [..._allEntries];
}

/** 按关键词搜索（匹配 originalText / vernacular / keywords） */
export function searchKnowledge(keyword: string): ClassicKnowledgeEntry[] {
  const lk = keyword.toLowerCase();
  return _allEntries.filter(e =>
    e.originalText.includes(keyword) ||
    e.vernacular.includes(keyword) ||
    e.keywords.some(k => k.includes(lk))
  );
}

/** 按古籍来源获取 */
export function getBySource(source: ClassicSource): ClassicKnowledgeEntry[] {
  return _allEntries.filter(e => e.source === source);
}

/** 按分类获取 */
export function getByCategory(category: ClassicCategory): ClassicKnowledgeEntry[] {
  return _allEntries.filter(e => e.category === category);
}

/** 按适用格局获取 */
export function getByPattern(pattern: string): ClassicKnowledgeEntry[] {
  return _allEntries.filter(e =>
    e.applicablePattern && e.applicablePattern.includes(pattern)
  );
}

/** 随机获取一条 */
export function getRandomQuote(): ClassicKnowledgeEntry {
  return _allEntries[Math.floor(Math.random() * _allEntries.length)];
}

/** 获取相关条目（按关键词匹配度排序） */
export function getRelatedQuotes(keywords: string[]): ClassicKnowledgeEntry[] {
  const scored = _allEntries.map(e => {
    let score = 0;
    for (const kw of keywords) {
      if (e.keywords.includes(kw)) score += 3;
      if (e.originalText.includes(kw)) score += 2;
      if (e.vernacular.includes(kw)) score += 1;
      if (e.chapter.includes(kw)) score += 1;
    }
    return { entry: e, score };
  });
  return scored.filter(s => s.score > 0).sort((a, b) => b.score - a.score).map(s => s.entry);
}

/** 获取各来源统计 */
export function getKnowledgeStats(): { source: string; count: number }[] {
  const map = new Map<string, number>();
  for (const e of _allEntries) {
    map.set(e.source, (map.get(e.source) || 0) + 1);
  }
  return [...map.entries()].map(([source, count]) => ({ source, count }));
}
