export function getCaseDataStats(): {
  totalCases: number
  bySource: Record<string, number>
  byCategory: Record<string, number>
  byTrustLevel: Record<string, number>
  byDayElement: Record<string, number>
  verifiedCases: number
} {
  const bySource: Record<string, number> = {}
  const byCategory: Record<string, number> = {}
  const byTrustLevel: Record<string, number> = {}
  const byDayElement: Record<string, number> = {}
  let verifiedCases = 0

  for (const c of CASE_DATA) {
    // source
    bySource[c.source] = (bySource[c.source] || 0) + 1
    // category
    for (const cat of c.category) {
      byCategory[cat] = (byCategory[cat] || 0) + 1
    }
    // trustLevel
    byTrustLevel[c.trustLevel] = (byTrustLevel[c.trustLevel] || 0) + 1
    // dayElement
    byDayElement[c.dayElement] = (byDayElement[c.dayElement] || 0) + 1
    // verified
    if (c.verifiedDimensions && (
      c.verifiedDimensions.geJu || c.verifiedDimensions.wangShuai ||
      c.verifiedDimensions.yongShen || c.verifiedDimensions.marriage ||
      c.verifiedDimensions.career
    )) {
      verifiedCases++
    }
  }

  return {
    totalCases: CASE_DATA.length,
    bySource,
    byCategory,
    byTrustLevel,
    byDayElement,
    verifiedCases,
  }
}