/**
 * 获取指定事件类型的判断规则
 * @param type 事件类型
 * @returns 对应规则，未找到则返回 undefined
 */
export function getEventRule(type: FortuneEventType): FortuneEventRule | undefined {
  return FORTUNE_EVENT_RULES.find((rule) => rule.type === type)
}

/**
 * 获取指定事件类型的 AI 解释知识库条目
 * @param type 事件类型
 * @returns 对应知识库条目，未找到则返回 undefined
 */
export function getEventKB(type: FortuneEventType): FortuneEventKBEntry | undefined {
  return FORTUNE_EVENT_KB[type]
}