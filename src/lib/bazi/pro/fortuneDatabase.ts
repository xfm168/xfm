/**
 * 检测两组干支之间的所有关系（冲、刑、害、破、合等）
 *
 * 算法逻辑：
 * 1. 天干：检测五合、天干相冲
 * 2. 地支：检测六合、三合局、三会局、六冲、六害、三刑、自刑、破
 * 3. 特殊关系：伏吟（干支完全相同）、反吟（天干冲 + 地支冲）
 *
 * @returns RelationInfo 包含所有检测到的关系及详细描述
 */
export function detectRelations(
  gan1: HeavenlyStem,
  zhi1: EarthlyBranch,
  gan2: HeavenlyStem,
  zhi2: EarthlyBranch,
): RelationInfo {
  const chong: string[] = []
  const xing: string[] = []
  const hai: string[] = []
  const po: string[] = []
  const he: string[] = []
  const sanHe: string[] = []
  const sanHui: string[] = []
  const liuHe: string[] = []
  const ganChong: string[] = []
  const ganHe: string[] = []
  const fuYin: string[] = []
  const fanYin: string[] = []

  // ── 天干五合 ──
  for (const [g1, g2, element] of TIAN_GAN_WU_HE) {
    if ((gan1 === g1 && gan2 === g2) || (gan1 === g2 && gan2 === g1)) {
      ganHe.push(`${gan1}${gan2}五合化${element}`)
      he.push(`${gan1}${gan2}五合`)
      break
    }
  }

  // ── 天干相冲 ──
  for (const [g1, g2] of TIAN_GAN_CHONG) {
    if ((gan1 === g1 && gan2 === g2) || (gan1 === g2 && gan2 === g1)) {
      ganChong.push(`${gan1}${gan2}冲`)
      break
    }
  }

  // ── 地支六合 ──
  for (const [z1, z2, element] of DI_ZHI_LIU_HE) {
    if ((zhi1 === z1 && zhi2 === z2) || (zhi1 === z2 && zhi2 === z1)) {
      liuHe.push(`${zhi1}${zhi2}六合化${element}`)
      he.push(`${zhi1}${zhi2}六合`)
      break
    }
  }

  // ── 三合局（两支属于同一三合 → 半合） ──
  for (const [z1, z2, z3, element] of DI_ZHI_SAN_HE) {
    const set = new Set([z1, z2, z3])
    if (set.has(zhi1) && set.has(zhi2)) {
      sanHe.push(`${zhi1}${zhi2}为${element}局半合`)
      he.push(`${zhi1}${zhi2}半合`)
      break
    }
  }

  // ── 三会局 ──
  for (const [z1, z2, z3, element] of DI_ZHI_SAN_HUI) {
    const set = new Set([z1, z2, z3])
    if (set.has(zhi1) && set.has(zhi2)) {
      sanHui.push(`${zhi1}${zhi2}为${element}方半会`)
      he.push(`${zhi1}${zhi2}半会`)
      break
    }
  }

  // ── 六冲 ──
  for (const [z1, z2] of DI_ZHI_LIU_CHONG) {
    if ((zhi1 === z1 && zhi2 === z2) || (zhi1 === z2 && zhi2 === z1)) {
      chong.push(`${zhi1}${zhi2}冲`)
      break
    }
  }

  // ── 六害 ──
  for (const [z1, z2] of DI_ZHI_LIU_HAI) {
    if ((zhi1 === z1 && zhi2 === z2) || (zhi1 === z2 && zhi2 === z1)) {
      hai.push(`${zhi1}${zhi2}害`)
      break
    }
  }

  // ── 三刑 ──
  for (const [z1, z2, z3] of DI_ZHI_SAN_XING) {
    const set = new Set([z1, z2, z3])
    if (set.has(zhi1) && set.has(zhi2)) {
      const name = (set.has('寅') ? '无恩之刑' : '恃势之刑')
      xing.push(`${zhi1}${zhi2}${name}`)
      break
    }
  }

  // ── 自刑 ──
  if (zhi1 === zhi2 && DI_ZHI_ZI_XING.includes(zhi1)) {
    xing.push(`${zhi1}自刑`)
  }

  // ── 破 ──
  for (const [z1, z2] of DI_ZHI_PO) {
    if ((zhi1 === z1 && zhi2 === z2) || (zhi1 === z2 && zhi2 === z1)) {
      po.push(`${zhi1}${zhi2}破`)
      break
    }
  }

  // ── 伏吟 ──
  if (gan1 === gan2 && zhi1 === zhi2) {
    fuYin.push(`${gan1}${zhi1}伏吟`)
  }

  // ── 反吟（天干冲+地支冲） ──
  const hasGanChong = TIAN_GAN_CHONG.some(
    ([g1, g2]) => (gan1 === g1 && gan2 === g2) || (gan1 === g2 && gan2 === g1),
  )
  const hasZhiChong = DI_ZHI_LIU_CHONG.some(
    ([z1, z2]) => (zhi1 === z1 && zhi2 === z2) || (zhi1 === z2 && zhi2 === z1),
  )
  if (hasGanChong && hasZhiChong) {
    fanYin.push(`${gan1}${zhi1}↔${gan2}${zhi2}反吟`)
  }

  return { chong, xing, hai, po, he, sanHe, sanHui, liuHe, ganChong, ganHe, fuYin, fanYin }
}

