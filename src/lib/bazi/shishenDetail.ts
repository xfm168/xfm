function getOvercomer(el: FiveElement): FiveElement {
  for (const [k, v] of Object.entries(OVERCOME)) { if (v === el) return k as FiveElement }
  return '土'
}

function getGenerator(el: FiveElement): FiveElement {
  for (const [k, v] of Object.entries(GENERATE)) { if (v === el) return k as FiveElement }
  return '水'
}

/** 十神 → 五行（相对于日主） */
function getShenShiElement(shen: ShenShi, dayGanElement: FiveElement): FiveElement {
  const map: Record<ShenShi, FiveElement> = {
    '比肩': dayGanElement,
    '劫财': dayGanElement,
    '食神': GENERATE[dayGanElement],
    '伤官': GENERATE[dayGanElement],
    '偏财': OVERCOME[dayGanElement],
    '正财': OVERCOME[dayGanElement],
    '偏官': getOvercomer(dayGanElement),
    '正官': getOvercomer(dayGanElement),
    '偏印': getGenerator(dayGanElement),
    '正印': getGenerator(dayGanElement),
  }
  return map[shen]
}

