// ═══════════════════════════════════════════════════════════════
// 位置提取 — 扫描四柱天干 + 地支藏干，收集十神出现位置
// ═══════════════════════════════════════════════════════════════

function collectShenShenPositions(
  shen: ShenShi,
  relatedShens: Record<string, string>,
  chart: BaZiChart | any,
): string[] {
  const positions: string[] = []

  // 四柱天干
  const ganPillars: { key: string; gan: HeavenlyStem }[] = [
    { key: '年干', gan: chart.sixLines?.year?.gan },
    { key: '月干', gan: chart.sixLines?.month?.gan },
    { key: '日干', gan: chart.sixLines?.day?.gan },
    { key: '时干', gan: chart.sixLines?.hour?.gan },
  ]
  for (const { key, gan } of ganPillars) {
    if (gan && relatedShens[gan] === shen) {
      positions.push(key)
    }
  }

  // 四柱地支藏干
  const zhiPillars: { key: string; zhi: EarthlyBranch }[] = [
    { key: '年支', zhi: chart.sixLines?.year?.zhi },
    { key: '月支', zhi: chart.sixLines?.month?.zhi },
    { key: '日支', zhi: chart.sixLines?.day?.zhi },
    { key: '时支', zhi: chart.sixLines?.hour?.zhi },
  ]
  for (const { key, zhi } of zhiPillars) {
    if (!zhi) continue
    const cangGan = chart.cangGan?.[zhi] || CANG_GAN_TABLE[zhi]
    if (!cangGan) continue
    if (cangGan.ben && relatedShens[cangGan.ben] === shen) {
      positions.push(`${key}藏干`)
    }
  }

  return positions
}

// ═══════════════════════════════════════════════════════════════
// 喜忌判断
// ═══════════════════════════════════════════════════════════════

function isShenShiFavorable(
  shenElement: FiveElement,
  chart: BaZiChart | any,
): boolean {
  const xiYong = chart.xiYongShen
  if (!xiYong) return false
  const { bestElement, usage, happiness, avoidedElements, enemyElements } = xiYong as any
  // 喜神 / 用神 / 闲神 → 喜
  if (bestElement === shenElement || usage === shenElement || happiness === shenElement) return true
  // 忌神 / 仇神 → 忌
  if (avoidedElements?.includes(shenElement) || enemyElements?.includes(shenElement)) return false
  // 闲神默认中性偏喜
  return true
}

