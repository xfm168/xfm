function detectRadical(char: string): string {
  const map = [
    '氵', '扌', '艹', '灬', '辶', '阝', '忄', '犭', '饣', '讠', '纟', '钅', '衤', '礻',
    '牜', '月', '木', '火', '土', '金', '水', '日', '山', '石', '田', '女', '心', '目',
    '口', '虫', '鸟', '鱼', '宀', '广', '门', '雨',
  ]
  for (const rad of map) {
    if (rad.length === 1 && char.length === 1 && char.includes(rad)) return rad
  }
  return ''
}

/** 估算罕见字康熙笔画（确定性，部首本画 + 编码哈希） */
function estimateStrokes(char: string): number {
  const looked = KANGXI_STROKES[char]
  if (looked) return looked
  const rad = detectRadical(char)
  const base = RADICAL_BASE_STROKES[rad] ?? 0
  const code = char.charCodeAt(0)
  const offset = (code % 13) + 1 // 1..13
  return Math.min(24, Math.max(4, base + offset))
}

function getStrokes(char: string, explicit?: number): number {
  if (explicit && explicit > 0) return explicit
  if (KANGXI_STROKES[char]) return KANGXI_STROKES[char]
  return estimateStrokes(char)
}

function getGender(char: string, code?: string): Gender {
  if (code) return parseGender(code)
  if (GENDER_HINT[char]) return GENDER_HINT[char]
  return 'neutral'
}

function getMeaning(char: string, explicit?: string, element?: FiveElement): string {
  if (explicit) return explicit
  if (MEANING_HINT[char]) return MEANING_HINT[char]
  return element ? ELEMENT_MEANING[element] : '寓意吉祥，可用为名'
}

/** 解析紧凑基础行 */
function parseBaseRow(row: string, element: FiveElement): CharInfo | null {
  const parts = row.split('|')
  if (parts.length < 1) return null
  const char = parts[0]
  if (!char) return null
  const kxStrokes = parts[1] ? parseInt(parts[1], 10) : undefined
  const gender = parts[2]
  const meaning = parts[3]
  return {
    char,
    kxStrokes: getStrokes(char, Number.isNaN(kxStrokes) ? undefined : kxStrokes),
    element,
    meaning: getMeaning(char, meaning, element),
    gender: getGender(char, gender),
  }
}

