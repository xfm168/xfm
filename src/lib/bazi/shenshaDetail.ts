// 神煞名称标准化（将 "桃花煞（年支查）" 等映射为标准名）
// ═══════════════════════════════════════════════════════════════

function normalizeShenShaName(rawName: string): string {
  // "桃花煞（年支查）" / "桃花煞（日支查）" → "桃花"
  if (rawName.includes('桃花')) return '桃花'
  if (rawName.includes('红鸾')) return '红鸾'
  if (rawName.includes('天喜')) return '天喜'
  if (rawName.includes('天乙贵人')) return '天乙贵人'
  if (rawName.includes('文昌')) return '文昌'
  if (rawName.includes('华盖')) return '华盖'
  if (rawName.includes('羊刃')) return '羊刃'
  if (rawName.includes('劫煞')) return '劫煞'
  if (rawName.includes('空亡')) return '空亡'
  if (rawName.includes('孤辰')) return '孤辰'
  if (rawName.includes('寡宿')) return '寡宿'
  if (rawName.includes('驿马')) return '驿马'
  return rawName
}

/** 获取查法类型（年支查/日支查/日干查等） */
function extractQueryMethod(rawName: string): string {
  if (rawName.includes('年支查')) return '年支查'
  if (rawName.includes('日支查')) return '日支查'
  if (rawName.includes('日柱查')) return '日柱查'
  if (rawName.includes('年柱查')) return '年柱查'
  return '综合'
}

