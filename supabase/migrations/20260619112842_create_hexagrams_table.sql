-- ══════════════════════════════════════════
--  六十四卦主数据表
-- ══════════════════════════════════════════
CREATE TABLE hexagrams (
  id            uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  number        smallint NOT NULL UNIQUE CHECK (number BETWEEN 1 AND 64),
  name          text NOT NULL,
  symbol        text NOT NULL,           -- Unicode ䷀–䷿
  upper_trigram text NOT NULL,           -- 上卦名
  lower_trigram text NOT NULL,           -- 下卦名
  lines         text[6] NOT NULL,        -- 六爻，index 0=初爻(底), 5=上爻(顶)
  description   text NOT NULL,           -- 卦辞
  fortune       text NOT NULL,           -- 总运
  career        text NOT NULL,
  wealth        text NOT NULL,
  love          text NOT NULL,
  health        text NOT NULL,
  advice_do     text[] NOT NULL DEFAULT '{}',
  advice_dont   text[] NOT NULL DEFAULT '{}',
  created_at    timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE hexagrams ENABLE ROW LEVEL SECURITY;

-- 所有人只读，不允许客户端写入
CREATE POLICY "hexagrams_select_public" ON hexagrams
  FOR SELECT USING (true);

